def _cmetrics() -> int:
    if WINVER < 0x0500 and (WINNT is None or WINNT < 0x0400):
        return 76
    elif WINVER == 0x0500:
        return 83
    elif WINVER == 0x0501:
        return 91
    elif WINVER == 0x0600:
        return 93
    else:
        return 97


