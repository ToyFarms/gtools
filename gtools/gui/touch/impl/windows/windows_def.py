def _keylast() -> int:
    if WINNT > 0x0501:
        return 0x0109
    else:
        return 0x0108


def _mouselast() -> int:
    if WINNT >= 0x0600:
        return 0x020E
    elif WINNT >= 0x0500:
        return 0x020D
    elif WINNT >= 0x0400:
        return 0x020A
    else:
        return 0x0209


